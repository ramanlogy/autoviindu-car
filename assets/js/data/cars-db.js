window.buildSearchIndex = function () {
  return (window.CARS_DB || []).map(car => ({
    id: car.id, slug: car.slug,
    searchText: [car.brand, car.model, car.type, car.body, car.year,
      car.variants.map(v => v.name).join(' '), car.tagline, car.overview
    ].join(' ').toLowerCase(),
    display: `${car.brand} ${car.model}`,
    price: car.variants[0].price,
    badge: car.badge, image: car.images[0],
    year: car.year, type: car.type, body: car.body
  }));
};